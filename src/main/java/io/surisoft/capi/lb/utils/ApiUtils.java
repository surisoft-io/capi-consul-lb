    public String getApiId(Api api) {
        JsonObject jsonObject = new JsonObject();
        jsonObject.put("apiName", api.getName());
        jsonObject.put("apiContext", api.getContext());
        if(api.getHttpMethod() == null || api.getHttpMethod().equals(HttpMethod.ALL)) {
            api.setHttpMethod(HttpMethod.ALL);
            jsonObject.put("httpMethod", HttpMethod.ALL);
        } else {
            jsonObject.put("httpMethod", api.getHttpMethod().getMethod());
        }
        return new String(Base64.getEncoder().encode(jsonObject.toJson().getBytes()));
    }

    public String getApiId(String apiName) {
        JsonObject jsonObject = new JsonObject();
        jsonObject.put("apiName", apiName);
        jsonObject.put("apiContext", apiName);
        jsonObject.put("httpMethod", HttpMethod.ALL);
        return new String(Base64.getEncoder().encode(jsonObject.toJson().getBytes()));
    }

    public boolean isMappingDifferent(Api existingApi, Mapping mapping) {
        if(existingApi.getMappingList().isEmpty()) {
            return true;
        }
        for(Mapping existingMapping : existingApi.getMappingList()) {
            if(existingMapping.getHostname().equals(mapping.getHostname()) && (existingMapping.getPort() == mapping.getPort())) {
                return true;
            }
        }
        return false;
    }

    public Mapping consulObjectToMapping(ConsulObject consulObject) {
        String host = consulObject.getServiceAddress();
        int port = consulObject.getServicePort();
        Mapping mapping = new Mapping();
        mapping.setHostname(host);
        mapping.setPort(port);
        mapping.setRootContext("/");
        mapping.setIngress(true);
        return mapping;
    }

    public void removeUnusedApi(CamelContext camelContext, RouteUtils routeUtils, ApiCacheManager apiCacheManager, List<String> serviceNameList) throws Exception {
        for(Map.Entry<String, Api> api : apiCacheManager.getCachedApi().entrySet()) {
            if(!serviceNameList.contains(api.getValue().getName())) {
                apiCacheManager.getCachedApi().remove(api);
                List<String> apiRouteIdList = routeUtils.getAllRouteIdForAGivenApi(api.getValue());
                for(String routeId : apiRouteIdList) {
                    camelContext.getRouteController().stopRoute(routeId);
                    camelContext.removeRoute(routeId);
                }
            }
        }
    }

    public void updateExistingApi(Api existingApi, Api incomingApi, ApiCacheManager apiCacheManager, RouteUtils routeUtils, CamelContext camelContext, StickySessionCacheManager stickySessionCacheManager) {

        if(isMappingChanged(existingApi.getMappingList(), incomingApi.getMappingList())) {
            log.trace("Changes detected for API: {}, redeploying routes.", existingApi.getId());
            existingApi.setMappingList(incomingApi.getMappingList());
            apiCacheManager.getCachedApi().put(existingApi.getId(), existingApi);

            try {
                List<String> apiRouteIdList = routeUtils.getAllRouteIdForAGivenApi(existingApi);
                for(String routeId : apiRouteIdList) {
                    camelContext.getRouteController().stopRoute(routeId);
                    camelContext.removeRoute(routeId);
                    camelContext.addRoutes(new SingleRouteProcessor(camelContext, incomingApi, routeUtils, routeId, stickySessionCacheManager));
                }
            } catch (Exception e) {
                e.printStackTrace();
            }


        } else {
            log.trace("No changes detected for API: {}.", existingApi.getId());
        }
    }

